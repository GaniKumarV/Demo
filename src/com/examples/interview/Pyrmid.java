package com.examples.interview;

public class Pyrmid {
	
	public static void main(String[] args) {
		
		
		for(int i=1;i<=3;i++) {
			
			for(int j=1;j<=3;j++) {
				
				System.out.println(i+" "+j);
			}
				
		}
		
		
		for(int k=1;k<=6;k++) {
			
			for(int l=1;l<=k;l++) {
				
				System.out.print("*");
			}
			System.out.println();
		}
	}

}
